fun main() {
    // variable berisifat mutable
    var name = "Abdan Zaki Alfian"
    name = "Zaki"
    println(name)

    // variable bersifat immutable
    val age = 21

    // data type menentukan hasil operasi
    val firstWord = "Belajar "
    val lastWord = "Kotlin"
    println(firstWord + lastWord)

    val valueA = 10
    val valueB = 20
    println(valueA + valueB)
}